document.addEventListener("DOMContentLoaded", function () {
  renderContent();

  const images = document.querySelectorAll("#content__imageContainer img");
  const prevBtn = document.getElementById("prevBtn");
  const nextBtn = document.getElementById("nextBtn");
  let currentIndex = 0;
  
  function showImage(index, direction) {
    images.forEach((image, i) => {
      if (i === index) {
        image.style.opacity = 1; /* –î–µ–ª–∞–µ–º –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–µ –≤–∏–¥–∏–º—ã–º */
        image.style.display = "block";
      } else {
        image.style.opacity = 0; /* –ü—Ä—è—á–µ–º –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–µ */
        image.style.display = "none";
      }
    });

    // –ü—Ä–∏–º–µ–Ω—è–µ–º –∞–Ω–∏–º–∞—Ü–∏—é –≤ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏ –æ—Ç –Ω–∞–ø—Ä–∞–≤–ª–µ–Ω–∏—è
    if (direction === "prev") {
      images[index].style.transform = "translateX(-100%)";
    } else {
      images[index].style.transform = "translateX(100%)";
    }

    // –ó–∞–ø—É—Å–∫–∞–µ–º –∞–Ω–∏–º–∞—Ü–∏—é
    requestAnimationFrame(() => {
      images[index].style.transform = "translateX(0)";
    });
  }

  function showPrev() {
    currentIndex = (currentIndex - 1 + images.length) % images.length;
    showImage(currentIndex, "prev");
  }

  function showNext() {
    currentIndex = (currentIndex + 1) % images.length;
    showImage(currentIndex, "next");
  }

  prevBtn.addEventListener("click", showPrev);
  nextBtn.addEventListener("click", showNext);
  
  // –ü–æ–∫–∞–∑—ã–≤–∞–µ–º –ø–µ—Ä–≤–æ–µ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–µ –ø—Ä–∏ –∑–∞–≥—Ä—É–∑–∫–µ —Å—Ç—Ä–∞–Ω–∏—Ü—ã
  showImage(currentIndex);
});
